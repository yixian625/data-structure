# Utilities Package
# This package contains helper modules, including:

# - `NodeClass.py`: class definition of tree nodes to store the frequency information
# - `PriorityListClass.py`: class definition for the array that stores the tree nodes and sort them based on priority
# - `TreeFunctions.py`: functions to traverse the Huffman encoding tree, find the corresponding binary code for \
# letters, and decode binary codes to letters
# - `TableCreate.py`: functions to detect whether the source file is a frequency table or text, and to create the \
# frequency table from a source text
