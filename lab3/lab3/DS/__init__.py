# Data Structure Package
# This Package contains the core data structure classes of the main package, including:

# - `NodeClass.py`: class definition of tree nodes to store the frequency information
# - `PriorityListClass.py`: class definition for the array that stores the tree nodes and sort them based on priority
# - `HuffmanTreeClass.py`: class definition for the Huffman encoding tree and its associated methods, \
# including building the priority array for the letters, building the tree, and traversing the tree
